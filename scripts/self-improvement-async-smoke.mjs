import { spawn } from "node:child_process";
import { existsSync } from "node:fs";
import path from "node:path";
import { setTimeout as delay } from "node:timers/promises";
import { DatabaseSync } from "node:sqlite";

const root = process.cwd();
const port = Number(process.env.DATASWARM_SELF_IMPROVEMENT_ASYNC_PORT ?? 3221);
const baseUrl = `http://localhost:${port}`;
const dataDir = path.resolve(root, process.env.DATASWARM_DATA_DIR ?? "data");
const dbPath = path.join(dataDir, "dataswarm.sqlite");
const results = [];
let server;

expect("sqlite database exists", existsSync(dbPath), dbPath);
if (!existsSync(dbPath)) {
  finish();
}

const db = new DatabaseSync(dbPath);
const cleanup = [];

try {
  const run = db
    .prepare("SELECT id, conversation_id, task_id FROM runs ORDER BY created_at DESC LIMIT 1")
    .get();
  expect("latest run exists", Boolean(run?.id), JSON.stringify(run ?? null));
  if (!run?.id) {
    finish();
  }

  const evalId = `eval_async_smoke_${Date.now()}`;
  const checkId = `e2b_template_async_smoke_check_${Date.now()}`;
  const now = new Date().toISOString();
  const checks = [
    {
      id: checkId,
      label: "E2B template async self-improvement smoke check",
      passed: false,
      detail: "Synthetic failed E2B template check for async self-improvement verification.",
    },
  ];
  db.prepare(
    `INSERT INTO eval_results
     (id, tenant_id, project_id, run_id, artifact_id, eval_type, status, score, summary, checks_json, trace_span_id, metadata_json, created_at, updated_at)
     VALUES (?, 'ten_default', 'prj_default', ?, NULL, 'run_health', 'completed', 0.5, ?, ?, NULL, ?, ?, ?)`,
  ).run(
    evalId,
    run.id,
    "[smoke] async self-improvement eval",
    JSON.stringify(checks),
    JSON.stringify({ visibility: "internal", schedule: "async_smoke" }),
    now,
    now,
  );
  cleanup.push(() => db.prepare("DELETE FROM self_improvement_candidates WHERE eval_result_id = ?").run(evalId));
  cleanup.push(() => db.prepare("DELETE FROM eval_results WHERE id = ?").run(evalId));
  expect("smoke eval inserted", true, evalId);

  if (process.env.DATASWARM_SELF_IMPROVEMENT_ASYNC_SKIP_BUILD !== "1") {
    await runProductionBuild();
  }
  server = spawn("npm", ["--prefix", "apps/web", "run", "start", "--", "-p", String(port)], {
    cwd: root,
    env: {
      ...process.env,
      DATASWARM_DATA_DIR: "../../data",
      DATASWARM_WORKSPACE_ROOT: "../..",
    },
    stdio: ["ignore", "pipe", "pipe"],
  });
  const output = [];
  server.stdout.on("data", (chunk) => output.push(String(chunk)));
  server.stderr.on("data", (chunk) => output.push(String(chunk)));
  await waitForHealth(output);

  const first = await postJson(`/api/runs/${run.id}/improvements`, {
    action: "run_async_analysis",
    eval_result_id: evalId,
  });
  expect("async analysis API returns candidate", first?.analysis?.candidates?.length === 1, JSON.stringify(first));

  const second = await postJson(`/api/runs/${run.id}/improvements`, {
    action: "run_async_analysis",
    eval_result_id: evalId,
  });
  expect("async analysis API is idempotent", second?.analysis?.candidates?.length === 1, JSON.stringify(second));

  const candidateRows = db
    .prepare(
      `SELECT id, status, evidence_json, proposal_json, verification_plan_json
       FROM self_improvement_candidates
       WHERE run_id = ? AND eval_result_id = ?`,
    )
    .all(run.id, evalId);
  expect("one candidate persisted for eval", candidateRows.length === 1, JSON.stringify(candidateRows));
  const candidate = candidateRows[0];
  const evidence = parseJson(candidate?.evidence_json, {});
  const proposal = parseJson(candidate?.proposal_json, {});
  const verificationPlan = parseJson(candidate?.verification_plan_json, {});
  expect("candidate evidence references failed check", evidence.check_id === checkId, JSON.stringify(evidence));
  expect("candidate generated by async analysis", proposal.generated_by === "self_improvement.analysis", JSON.stringify(proposal));
  expect(
    "E2B candidate verification plan uses template gates",
    Array.isArray(verificationPlan.required_commands) &&
      verificationPlan.required_commands.includes("node scripts/e2b-template-smoke.mjs") &&
      verificationPlan.required_commands.includes("node scripts/e2b-template-receipt-smoke.mjs") &&
      verificationPlan.required_commands.includes("node scripts/e2b-readiness-smoke.mjs") &&
      verificationPlan.required_commands.includes("node scripts/e2b-live-receipt-smoke.mjs"),
    JSON.stringify(verificationPlan),
  );

  const events = db
    .prepare(
      `SELECT event_type
       FROM run_events
       WHERE run_id = ? AND event_type IN (
         'self_improvement.analysis.started',
         'self_improvement.analysis.completed',
         'self_improvement.candidates.queued'
       )`,
    )
    .all(run.id)
    .map((row) => row.event_type);
  expect(
    "async analysis events persisted",
    events.includes("self_improvement.analysis.started") &&
      events.includes("self_improvement.analysis.completed") &&
      events.includes("self_improvement.candidates.queued"),
    JSON.stringify(events),
  );
} finally {
  for (const item of cleanup.reverse()) {
    item();
  }
  db.close();
  if (server) {
    server.kill("SIGTERM");
    await delay(500);
    if (!server.killed) {
      server.kill("SIGKILL");
    }
  }
}

finish();

async function runProductionBuild() {
  const output = [];
  const child = spawn("npm", ["--prefix", "apps/web", "run", "build"], {
    cwd: root,
    env: {
      ...process.env,
      DATASWARM_DATA_DIR: "../../data",
      DATASWARM_WORKSPACE_ROOT: "../..",
    },
    stdio: ["ignore", "pipe", "pipe"],
  });
  child.stdout.on("data", (chunk) => output.push(String(chunk)));
  child.stderr.on("data", (chunk) => output.push(String(chunk)));
  const exitCode = await new Promise((resolve) => {
    child.on("exit", (code) => resolve(code ?? 1));
    child.on("error", () => resolve(1));
  });
  expect("production build refreshed", exitCode === 0, output.join("\n").slice(-3000));
  if (exitCode !== 0) {
    finish();
  }
}

async function waitForHealth(output) {
  const deadline = Date.now() + 45_000;
  while (Date.now() < deadline) {
    const response = await fetch(`${baseUrl}/api/system/snapshot`).catch(() => null);
    if (response?.ok) {
      expect("production server healthy", true, baseUrl);
      return;
    }
    if (server?.exitCode !== null && server?.exitCode !== undefined) {
      break;
    }
    await delay(500);
  }
  expect("production server healthy", false, output.join("\n").slice(-3000));
  finish();
}

async function postJson(route, body) {
  const response = await fetch(`${baseUrl}${route}`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(body),
  });
  const text = await response.text();
  try {
    return JSON.parse(text);
  } catch {
    return { status: response.status, text };
  }
}

function parseJson(value, fallback) {
  try {
    return JSON.parse(String(value ?? ""));
  } catch {
    return fallback;
  }
}

function expect(name, condition, detail = "") {
  results.push({ name, passed: Boolean(condition), detail });
  console.log(`${condition ? "PASS" : "FAIL"} ${name}: ${detail}`);
}

function finish() {
  const failed = results.filter((result) => !result.passed);
  if (failed.length > 0) {
    console.error(`\nSelf-improvement async smoke failed: ${failed.length}/${results.length} check(s) failed.`);
    process.exit(1);
  }
  console.log(`\nSelf-improvement async smoke passed: ${results.length}/${results.length} check(s) passed.`);
  process.exit(0);
}
